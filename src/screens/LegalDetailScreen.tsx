function renderContent(content: string): React.ReactNode[] {
  const parts = content.split(/\{ts1\}(.*?)\{\/ts1\}/s);
  return parts.map((part, i) => {
    if (i % 2 === 1) {
      return (
        <Text key={i} style={styles.sectionHeader}>
          {part}
        </Text>
      );
    }
    return (
      <Text key={i} style={styles.bodyText}>
        {part}
      </Text>
    );
  });
}

